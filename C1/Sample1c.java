class Sample1c {
    public static void main(String[] args){
        float pi_f = 3.141592f;
        double pi_d = 3.141592d;

        System.out.println(pi_f);
        System.out.println(pi_d);
        System.out.printf("%5.2f\n", pi_f);
        System.out.printf("%5.2f\n", pi_d);
        System.out.printf("%.3f\n", pi_f);
        System.out.printf("%.3f\n", pi_d);
    }    
}
